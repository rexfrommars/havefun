def mini_string_luck(code):
    code = ''.join([c for c in code if c in '+.'])
    mem = 0
    out = bytearray()
    l = len(code)
    p = 0

    while p < l:
        c = code[p:].find('.')
        if c >= 0:
            mem = (mem+c) % 256
            p += c + 1
        elif c < 0:
            break
        out.append(mem)

    result = out.decode(encoding='utf8')
    print(result)
    return result

