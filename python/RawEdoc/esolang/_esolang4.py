class BoolLuckMachine(object):
    _BL = 64
    _BLOCK = [False for _ in range(_BL)]

    def __init__(self, code, input_):
        self._code = ''.join([c for c in code if c in '+,;<>[]'])
        self._code_len = len(self._code)
        self._cp = 0
        self._data = [] + self._BLOCK
        self._data_len = self._BL
        self._dp = self._BL // 2
        self._input = self.prepare_inpute(input_)
        self._out = []
        print('@@ %s' % code)
        # print('-- %s' % input_)
        print('** %s' % self._input)

    @staticmethod
    def prepare_inpute(_input):
        return ''.join([''.join(reversed(('00000000' + bin(ord(c))[2:])[-8:])) for c in _input])

    @staticmethod
    def prepare_output(_output):
        mode = len(_output) % 8
        if mode is not 0:
            _output += '0' * (8 - mode)
        out = []
        for i in range(0, len(_output), 8):
            out.append(chr(int(''.join(reversed(_output[i:i + 8])), 2)))
        return ''.join(out)

    @staticmethod
    def _error():
        raise Exception('Something is wrong!')

    def flip(self):
        self._data[self._dp] = not self._data[self._dp]

    def read(self):
        if len(self._input) and self._dp < self._data_len:
            self._data[self._dp], self._input = self._input[0], self._input[1:]
            self._data[self._dp] = False if self._data[self._dp] == '0' else True
        else:
            self._data[self._dp] = False

    def out(self):
        self._out.append(self._data[self._dp])

    def inc_p(self):
        self._dp += 1
        if self._dp == self._data_len:
            self._data += self._BLOCK
            self._data_len += self._BL

    def dec_p(self):
        if self._dp == 0:
            self._data = self._BLOCK + self._data
            self._data_len += self._BL
            self._dp = self._BL
        self._dp -= 1

    def if0(self):
        if self._data[self._dp] is False:
            p, c = self._cp, 1
            while p < self._code_len:
                p += 1
                if self._code[p] == ']':
                    c -= 1
                    if c == 0:
                        break
                elif self._code[p] == '[':
                    c += 1
            self._cp = p + 1 if p < self._code_len else self._error()
        else:
            self._cp += 1

    def if1(self):
        if self._data[self._dp] is True:
            p, c = self._cp, 1
            while p >= 0:
                p -= 1
                if self._code[p] == '[':
                    c -= 1
                    if c == 0:
                        break
                elif self._code[p] == ']':
                    c += 1
            self._cp = p + 1 if p >= 0 else self._error()
        else:
            self._cp += 1

    def pace(self):
        cmd = self._code[self._cp]
        if cmd == '>':
            self._dp += 1
            if self._dp == self._data_len:
                self._data += self._BLOCK
                self._data_len += self._BL
        elif cmd == '<':
            if self._dp == 0:
                self._data = self._BLOCK + self._data
                self._data_len += self._BL
                self._dp = self._BL
            self._dp -= 1
        elif cmd == ',':
            if len(self._input) and self._dp < self._data_len:
                self._data[self._dp], self._input = self._input[0], self._input[1:]
                self._data[self._dp] = False if self._data[self._dp] == '0' else True
            else:
                self._data[self._dp] = False
        elif cmd == ';':
            self._out.append(self._data[self._dp])
        elif cmd == '+':
            self._data[self._dp] = not self._data[self._dp]
        elif cmd == '[':
            self.if0()
        elif cmd == ']':
            self.if1()

        # func = {
        #     '+': lambda: self.flip(),
        #     ',': lambda: self.read(),
        #     ';': lambda: self.out(),
        #     '<': lambda: self.dec_p(),
        #     '>': lambda: self.inc_p(),
        #     '[': lambda: self.if0(),
        #     ']': lambda: self.if1(),
        # }
        # func[cmd]()
        if cmd not in '[]':
            self._cp += 1

    def execute(self):
        while self._cp < self._code_len and 0 <= self._dp < self._data_len:
            self.pace()

    def get_output(self):
        result = self.prepare_output(''.join(['0' if x is False else '1' for x in self._out]))
        return result


def boolluck(code, inp=''):
    machine = BoolLuckMachine(code, inp)
    machine.execute()
    return machine.get_output()

