def ticker(code):
    code = ''.join([c for c in code if c in '><+-*/!'])
    out = []
    step = 1024
    capa = step
    mem = bytearray('\0' * capa, 'ascii')
    boundary = 1
    cursor = 0

    for c in code:
        if c == '>':
            if cursor == capa:
                mem += bytearray('\0' * step, 'ascii')
            cursor += 1

        elif c == '<':
            # if cursor == 0:
            cursor -= 1

        elif c == '+':
            if cursor < boundary:
                mem[cursor] = 0 if mem[cursor] == 255 else mem[cursor]+1

        elif c == '-':
            if cursor < boundary:
                mem[cursor] = 255 if mem[cursor] == 0 else mem[cursor]-1

        elif c == '/':
            mem[cursor] = 0

        elif c == '*':
            out.append(chr(mem[cursor]))

        elif c == '!':
            boundary += 1
        # else pass

    return ''.join(out)


